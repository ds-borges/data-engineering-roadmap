# 7. Crie um programa que calcule a média de dois números flutuantes fornecidos pelo usuário. #

# Pegando os 2 valores
print(" Digite 2 valores com ponto para descobrirmos a média")
valor_01 = float(input("Digite o primeiro valor: "))
valor_02 = float(input("Digite o segundo valor: "))

# Fazendo a média dos valores
valor_total = (valor_01 + valor_02) / 2
print(f"A soma dos valores é: {valor_total}") 