# 4. Faça um programa que peça dois números inteiros e imprima a divisão inteira do primeiro pelo segundo. #

# Pegando os valores
print(" Dividindo 2 valores")
valor_01 = int(input("Digite o primeiro valor: "))
valor_02 = int(input("Digite o segundo valor: "))

# Dividindo o primeiro pelo segundo
valor_total = valor_01 / valor_02

print(f"A divisão do primeiro pelo segundo é: {valor_total}") 