# 12. Crie um programa que receba o nome completo do usuário e imprima o nome com todas as letras minúsculas. #

# Recebendo a palavra ou a frase
print("Digite o nome para convertermos todo o texto para minúsculo")
nome = input("Digite o nome ")

# Convertendo para maiúsculo
nome_final = nome.lower()

print(f"O nome {nome} convertido para minúsculo é {nome_final}")