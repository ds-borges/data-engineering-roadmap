# 3. Desenvolva um programa que multiplique dois números fornecidos pelo usuário e mostre o resultado. #

# Pegando os valores
print(" Multiplicando 2 valores")
valor_01 = int(input("Digite o primeiro valor: "))
valor_02 = int(input("Digite o segundo valor: "))

# Multiplicando os valores
valor_total = valor_01 * valor_02

print(f"A multiplicação dos valores é: {valor_total}") 