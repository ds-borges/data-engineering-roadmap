# 11. Escreva um programa que receba uma string do usuário e a converta para maiúsculas.

# Recebendo a palavra ou a frase
print("Digite uma palavra ou uma frase para fazermos a conversão para maiúsculo")
entrada = input("Digite uma palavra ou uma frase: ")

# Convertendo para maiúsculo
resultado = entrada.upper()

print(f"A entrada {entrada} convertida para a maiúscula é {resultado}")