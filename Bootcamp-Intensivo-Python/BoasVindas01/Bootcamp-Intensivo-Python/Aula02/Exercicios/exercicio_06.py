# 6. Escreva um programa que receba dois números flutuantes e realize sua adição.  #

# Pegando os 2 valores
print(" Digite 2 valores com ponto para serem somados")
valor_01 = float(input("Digite o primeiro valor: "))
valor_02 = float(input("Digite o segundo valor: "))

# Somando os valores
valor_total = valor_01 + valor_02
print(f"A soma dos valores é: {valor_total}") 