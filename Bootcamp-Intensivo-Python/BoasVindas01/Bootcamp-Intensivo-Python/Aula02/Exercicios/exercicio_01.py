# Escreva um programa que soma dois números inteiros inseridos pelo usuário. #

# Pegando os 2 valores
print(" Digite 2 valores para serem somados")
valor_01 = int(input("Digite o primeiro valor: "))
valor_02 = int(input("Digite o segundo valor: "))

# Somando os valores
valor_total = valor_01 + valor_02
print(f"A soma dos valores é: {valor_total}") 